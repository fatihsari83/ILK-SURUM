package ders11_Wisequarter;

public class C09_ForLoop {
    public static void main(String[] args) {

        // 50 den baslayıp, 100 e kadar 100 dahil
        // sayıları yanyana aralarında 1 bosluk
        // olacak sekilde yazdırın



        // 3 basamaklı sayılardan 23 ile
        // bolunebılenleri yan yana
        // aralarında bir bosluk olacak sekilde yazdırın

        // 100 den baslayarak 1 e kadar sınırlar dahil
        // 7 ile bolunebılen sayıları yazdırın
        for (int i = 100; i >=1 ; i--) {
            if (i%7==0){
                System.out.print(i + " ");
            }
        }

    }

}
